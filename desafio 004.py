anything = input('Enter Anything: ')

print('What type is this? ', type(anything))
print('Is this alpha number? ', anything.isalnum())
print('Is this alphabetic? ', anything.isalpha())
print('Is this decimal? ', anything.isdecimal())
print('Is this numeric? ', anything.isnumeric())
print('Is this lower? ', anything.islower())
print('Is this upper?', anything.isupper())
print('Is this title?', anything.istitle())
print('Is this just space?', anything.isspace())









